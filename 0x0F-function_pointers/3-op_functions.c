#include "3-calc.h"

/**
 * op_add - adds 2 numbers
 * @a: integer
 * @b: integer
 * Return: result
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract 2 numbers
 * @a: integer
 * @b: integer
 * Return: result
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiple 2 numbers
 * @a: integer
 * @b: integer
 * Return: result
*/
int op_mul(int a, int b)
{
	return ( a * b);
}
/**
 * op_div - divide 2 numbers
 * @a: integer
 * @b: integer
 * Return: result
*/
int op_div(int a, int b)
{
	return ( a / b);
}
/**
 * op_mod - module a % b
 * @a: integer
 * @b: integer
 * Return: result
*/
int op_mod(int a, int b)
{
	return ( a % b);
}
